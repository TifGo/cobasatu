public class Persegi {
    
    // Atribut
    public double panjang;
    public double lebar;
    public double luas;
    
    // Konstruktor
    public Persegi(double panjang, double lebar) {
        this.panjang = panjang;
        this.lebar = lebar;
        this.luas = panjang * lebar / 0;
    }
    
}
